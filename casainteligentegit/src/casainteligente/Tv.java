package casainteligente;

public class Tv {
	private String marca;
	private boolean estado;
	
	public Tv() {
		
	}
	public String getmarca() {
		return this.marca;
	}
	public void setmarca(String marca) {
		this.marca = marca;
	}
	public boolean getestado() {
		return this.estado;
	}
	public void setestado(boolean estado) {
		this.estado = estado;
	}

}
